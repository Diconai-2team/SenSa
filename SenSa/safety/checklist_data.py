def get_all_item_keys() -> list[str]:
    """모든 체크 항목의 key 평탄 리스트. 제출 검증용."""
    keys: list[str] = []
    for category in CHECKLIST_ITEMS:
        for item in category["items"]:
            keys.append(item["key"])
    return keys


def get_total_count() -> int:
    """총 체크 항목 수 — UI 진행률 표시 등에 활용."""
    return len(get_all_item_keys())
